package ReplitQA;

public class Calc {

private int x,y;
private int result;

    public Calc() {
        x = x;
        y = y;
        result = result;
    }

    public int getResult()
    {
        return result;
    }

    public void setX(int x)
    {
        this.x = x;
    }

    public void setY(int y)
    {
        this.y = y;
    }
   public void plus()
    {
        result =x+y;
    }

    public void minus()
    {
        result = x-y;
    }

}
