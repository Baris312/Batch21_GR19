package ReplitQA;

public class Value {


public int val;
public boolean modified;

    public Value() {
        modified = modified;
        val=val;
    }

    public Value(int val) {
        this.val = val;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;

    }

    public boolean wasModified() {
        return modified;
    }

    public void setModified(boolean modified) {
        this.modified = modified;
    }
}
